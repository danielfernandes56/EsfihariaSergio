    public void chama_insert() throws ClassNotFoundException{
    String nome;
    String endereco;
    String complemento;
    int numero;
    String telefone;
    String celular;
            
    nome =  txtNome.getText();
    endereco = txtEnd.getText();
    complemento = txtComp.getText();
    numero = Integer.parseInt(txtNum.getText());
    telefone = JFtxtTel.getText();
    celular = JFtxtCel.getText();
    DaoCadCliente.insertCliente(nome, endereco, complemento, numero, telefone, celular);
    }

